void FOC_OpenLoopInit(foc_TypeDef *mfoc, float vbus, float uq, float fe_hz)
{
    mfoc->Udc   = vbus;
    mfoc->Ud    = 0.0f;
    mfoc->Uq    = uq;
    mfoc->fe    = fe_hz;
    mfoc->angle = 0.0f;

    FOC_AllOff();
}


